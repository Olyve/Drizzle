import UIKit

// MARK: - Add app colors to UIColor
extension UIColor {
  static var drizzleWhite: UIColor {
    return UIColor(red: (214/255), green: (208/255), blue: (202/255), alpha: 1)
  }
  
  static var drizzleDarkGray: UIColor {
    return UIColor(red: (68/255), green: (70/255), blue: (79/255), alpha: 1)
  }
  
  static var drizzleGreen: UIColor {
    return UIColor(red: (157/255), green: (176/255), blue: (109/255), alpha: 1)
  }
}
